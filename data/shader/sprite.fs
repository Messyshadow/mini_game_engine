/**
 * sprite.fs - 精灵渲染片段着色器
 * 
 * 用途：渲染带纹理的精灵（Sprite）
 * 功能：纹理采样、颜色调制、透明度裁剪
 */

#version 330 core

// ============================================================================
// 从顶点着色器接收的数据
// ============================================================================
in vec2 vTexCoord;  // 纹理坐标

// ============================================================================
// 输出
// ============================================================================
out vec4 FragColor;

// ============================================================================
// Uniform变量
// ============================================================================
uniform sampler2D uTexture; // 纹理采样器
uniform vec4 uColor;        // 颜色调制（与纹理颜色相乘）

// ============================================================================
// 主函数
// ============================================================================
void main() {
    // 从纹理采样颜色
    vec4 texColor = texture(uTexture, vTexCoord);
    
    // 透明度裁剪：丢弃几乎透明的像素
    if (texColor.a < 0.01) {
        discard;
    }
    
    // 最终颜色 = 纹理颜色 × 调制颜色
    FragColor = texColor * uColor;
}
