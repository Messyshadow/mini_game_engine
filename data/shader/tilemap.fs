/**
 * tilemap.fs - 瓦片地图渲染片段着色器
 * 
 * 用途：渲染瓦片地图的纹理
 * 特点：简单高效，支持透明瓦片
 */

#version 330 core

// ============================================================================
// 从顶点着色器接收的数据
// ============================================================================
in vec2 vTexCoord;

// ============================================================================
// 输出
// ============================================================================
out vec4 FragColor;

// ============================================================================
// Uniform变量
// ============================================================================
uniform sampler2D uTileset;  // 瓦片集纹理
uniform vec4 uTint;          // 可选的颜色叠加（默认白色）

// ============================================================================
// 主函数
// ============================================================================
void main() {
    vec4 texColor = texture(uTileset, vTexCoord);
    
    // 丢弃透明像素
    if (texColor.a < 0.01) {
        discard;
    }
    
    // 应用颜色叠加
    FragColor = texColor * uTint;
}
