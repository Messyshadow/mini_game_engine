// ============================================================================
// ImGui渲染函数 - 绘制调试UI
// ============================================================================

/**
 * RenderImGui函数：绘制ImGui界面
 * 
 * ImGui是一个"即时模式"GUI库：
 * - 每帧重新绘制所有UI元素
 * - 不需要保存UI状态
 * - 非常适合调试界面
 */
void RenderImGui() {
    // 获取引擎单例，用于访问引擎功能
    Engine& engine = Engine::Instance();
    
    // ========================================================================
    // 窗口1：引擎信息
    // ========================================================================
    
    // ImGui::Begin() 创建一个窗口，参数是窗口标题
    // 返回值表示窗口是否展开（可以用于优化，跳过折叠窗口的内容）
    ImGui::Begin("Engine Info");
    
    // 显示帧率（FPS = Frames Per Second）
    // GetFPS()返回当前帧率，通常为60左右
    ImGui::Text("FPS: %.1f", engine.GetFPS());
    
    // 显示帧时间（毫秒）
    // GetDeltaTime()返回秒，乘以1000转换为毫秒
    ImGui::Text("Frame Time: %.3f ms", engine.GetDeltaTime() * 1000.0f);
    
    // 退出按钮
    // ImGui::Button()返回true表示被点击
    if (ImGui::Button("Quit")) {
        engine.Quit();  // 调用引擎的退出函数
    }
    
    // ImGui::End() 结束当前窗口
    // 必须与Begin()配对使用
    ImGui::End();
    
    // ========================================================================
    // 窗口2：向量数学演示
    // ========================================================================
    
    ImGui::Begin("Vector Math Demo");
    
    // DragFloat2：可拖动的2D向量输入
    // 参数：标签、指向float的指针、拖动速度
    // &vectorA.x 获取x分量的地址，ImGui会同时修改x和y
    ImGui::DragFloat2("Vector A", &vectorA.x, 0.1f);
    ImGui::DragFloat2("Vector B", &vectorB.x, 0.1f);
    
    // 分隔线
    ImGui::Separator();
    ImGui::Text("Operations:");
    
    // ------------------------------------------------------------------------
    // 向量加法：A + B = (Ax+Bx, Ay+By)
    // ------------------------------------------------------------------------
    Vec2 sum = vectorA + vectorB;
    ImGui::Text("A + B = (%.2f, %.2f)", sum.x, sum.y);
    
    // ------------------------------------------------------------------------
    // 点积（内积）：A · B = Ax*Bx + Ay*By
    // 
    // 几何意义：
    // - 结果 > 0：两向量夹角 < 90° （大致同向）
    // - 结果 = 0：两向量垂直
    // - 结果 < 0：两向量夹角 > 90° （大致反向）
    // ------------------------------------------------------------------------
    float dot = vectorA.Dot(vectorB);
    ImGui::Text("A . B = %.3f", dot);
    
    // ------------------------------------------------------------------------
    // 叉积（2D中返回标量）：A × B = Ax*By - Ay*Bx
    // 
    // 几何意义：
    // - 结果 > 0：B在A的逆时针方向（左边）
    // - 结果 = 0：A和B平行
    // - 结果 < 0：B在A的顺时针方向（右边）
    // - |A × B| = 两向量构成的平行四边形面积
    // ------------------------------------------------------------------------
    float cross = vectorA.Cross(vectorB);
    ImGui::Text("A x B = %.3f", cross);
    
    // ------------------------------------------------------------------------
    // 向量长度（模）：|A| = √(Ax² + Ay²)
    // ------------------------------------------------------------------------
    ImGui::Text("|A| = %.3f", vectorA.Length());
    
    // ------------------------------------------------------------------------
    // 两向量夹角：θ = arccos(A·B / (|A|×|B|))
    // 
    // AngleBetween返回弧度，ToDegrees转换为角度
    // ------------------------------------------------------------------------
    float angle = Vec2::AngleBetween(vectorA, vectorB);
    ImGui::Text("Angle = %.1f degrees", ToDegrees(angle));
    
    ImGui::End();
}

// ============================================================================
// 主函数 - 程序入口
// ============================================================================

/**
 * main函数：程序入口点
 * 
 * 程序执行流程：
 * 1. 获取引擎单例
 * 2. 配置并初始化引擎
 * 3. 设置回调函数
 * 4. 进入主循环
 * 5. 清理并退出
 */
int main() {
    // 控制台输出启动信息
    std::cout << "Mini Game Engine - Example 01" << std::endl;
    
    // ------------------------------------------------------------------------
    // 步骤1：获取引擎单例
    // ------------------------------------------------------------------------
    // Engine使用单例模式，全局只有一个实例
    // Instance()返回该唯一实例的引用
    Engine& engine = Engine::Instance();
    
    // ------------------------------------------------------------------------
    // 步骤2：配置并初始化引擎
    // ------------------------------------------------------------------------
    // EngineConfig结构体包含引擎配置选项
    EngineConfig config;
    config.windowTitle = "Mini Engine - Vector Math";  // 窗口标题
    // 还可以设置：
    // config.windowWidth = 1280;   // 窗口宽度（默认1280）
    // config.windowHeight = 720;   // 窗口高度（默认720）
    // config.vsync = true;         // 垂直同步（默认true）
    
    // Initialize()创建窗口、初始化OpenGL和ImGui
    // 失败返回false，通常是显卡不支持或窗口创建失败
    if (!engine.Initialize(config)) {
        return -1;  // 初始化失败，退出程序
    }
    
    // ------------------------------------------------------------------------
    // 步骤3：设置回调函数
    // ------------------------------------------------------------------------
    // 这些函数会在主循环中被自动调用
    engine.SetUpdateCallback(Update);      // 每帧调用，用于更新逻辑
    engine.SetRenderCallback(Render);      // 每帧调用，用于渲染图形
    engine.SetImGuiCallback(RenderImGui);  // 每帧调用，用于渲染UI
    
    // ------------------------------------------------------------------------
    // 步骤4：进入主循环
    // ------------------------------------------------------------------------
    // Run()内部是一个while循环：
    // while (!shouldClose) {
    //     处理输入事件
    //     调用Update(deltaTime)
    //     清屏
    //     调用Render()
    //     调用RenderImGui()
    //     交换缓冲区
    // }
    engine.Run();
    
    // ------------------------------------------------------------------------
    // 步骤5：清理并退出
    // ------------------------------------------------------------------------
    // Shutdown()释放所有资源：窗口、OpenGL上下文、ImGui等
    engine.Shutdown();
    
    return 0;  // 正常退出
}
