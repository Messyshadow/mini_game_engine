using namespace MiniEngine;       // 引擎命名空间
using namespace MiniEngine::Math; // 数学库命名空间

// ============================================================================
// 全局变量
// ============================================================================

/**
 * 两个2D向量，用于演示向量运算
 * Vec2是一个简单的结构体，包含x和y两个float成员
 */
Vec2 vectorA(3.0f, 2.0f);  // 向量A：起点在原点，指向(3, 2)
Vec2 vectorB(1.0f, 4.0f);  // 向量B：起点在原点，指向(1, 4)

/**
 * 动画时间累加器
 * 用于记录程序运行的总时间，可用于创建动画效果
 */
